function getTargetFactors(page) {
    if (page == "agency") {
        return {
            "pageTarget": [1, 2],
        };
    } else if (page == "news") {
        return {
            "pageTarget": [1, 2],
        };
    } else if (page == "blog") {
        return {
            "pageTarget": [1, 2],
        };
    }
}